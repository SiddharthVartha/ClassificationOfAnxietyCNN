class Worker1(QThread):
    ImageUpdate = pyqtSignal(QImage)
    Capture = cv2.VideoCapture(0)

    def run(self):
        self.ThreadActive = True
        if(self.Capture.isOpened()):
            while self.ThreadActive:
                ret, frame = self.Capture.read()
                if ret:
                    # Image=cv2.cvtColor(frame,cv2.COLOR_BGR2RGB)
                    #FlippedImage=cv2.flip(Image, 1)
                    #ConvertToQtFormat=QImage(FlippedImage.data, FlippedImage.shape[1], FlippedImage.shape[0], QImage.Format_RGB888)
                    #Pic=ConvertToQtFormat.scaled (640, 360, Qt.KeepAspectRatio)
                    # self.ImageUpdate.emit(Pic)
                    face_detector = cv2.CascadeClassifier(
                        'haarcascades/haarcascade_frontalface_default.xml')
                    gray_frame = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)

                    # detect faces available on camera
                    num_faces = face_detector.detectMultiScale(
                        gray_frame, scaleFactor=1.3, minNeighbors=5)

                    # take each face available on the camera and Preprocess it
                    for (x, y, w, h) in num_faces:
                        #cv2.rectangle(frame, (x, y-50), (x+w, y+h+10), (0, 255, 0), 4)
                        roi_gray_frame = gray_frame[y:y + h, x:x + w]
                        cropped_img = np.expand_dims(np.expand_dims(
                            cv2.resize(roi_gray_frame, (48, 48)), -1), 0)

                        # predict the emotions
                        emotion_prediction = emotion_model.predict(cropped_img)
                        maxindex = int(np.argmax(emotion_prediction))
                        #cv2.putText(frame, emotion_dict[maxindex], (x+5, y-20), cv2.FONT_HERSHEY_SIMPLEX, 1, (255, 0, 0), 2, cv2.LINE_AA)
                        if(emotion_dict[maxindex] == "Angry"):
                            prediction[0] += 1
                        elif(emotion_dict[maxindex] == "Fearful"):
                            prediction[1] += 1
                        elif(emotion_dict[maxindex] == "Happy"):
                            prediction[2] += 1
                        elif(emotion_dict[maxindex] == "Sad"):
                            prediction[3] += 1
                        elif(emotion_dict[maxindex] == "Surprised"):
                            prediction[4] += 1
                    frame = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
                    ConvertToQtFormat = QImage(
                        frame.data, frame.shape[1], frame.shape[0], QImage.Format_RGB888)
                    Pic = ConvertToQtFormat.scaled(
                        640, 360, Qt.KeepAspectRatio)
                    self.ImageUpdate.emit(Pic)

    def stop(self):
        print("Inside Stop")
        self.ThreadActive = False
        self.Capture.release()
        cv2.destroyAllWindows()


